#Средствами языка Python сформировать два текстовых файла (.txt), содержащих по одной 
#последовательности из целых положительных и отрицательных чисел.
#Сформировать новый текстовый файл следующего вида, предварительно выполнив требуемую обработку элементов:

# Содержимое первого файла: 
# Отрицательные элементы: 
# Количество отрицательных элементов: 
# Среднее арифметическое: 

# Содержимое второго файла: 
# Положительные элементы: 
# Количество положительных элементов: 
# Сумма положительных элементов:

with open("файл1.txt", "w") as f1:
    f1.write("5 -3 7 -2 9 -8 4")

with open("файл2.txt", "w") as f2:
    f2.write("-1 3 -4 6 2 -6 10")

with open("файл1.txt", "r") as f1:
    числа1 = list(map(int, f1.read().split()))
    отрицательные = [x for x in числа1 if x < 0]
    кол_отриц = len(отрицательные)
    ср_отриц = sum(отрицательные) / кол_отриц if кол_отриц > 0 else 0

with open("файл2.txt", "r") as f2:
    числа2 = list(map(int, f2.read().split()))
    положительные = [x for x in числа2 if x > 0]
    кол_полож = len(положительные)
    сумма_полож = sum(положительные)

with open("результат.txt", "w") as f3:
    f3.write("Содержимое первого файла:\n")
    f3.write(" ".join(map(str, числа1)) + "\n")
    f3.write("Отрицательные элементы: " + " ".join(map(str, отрицательные)) + "\n")
    f3.write("Количество отрицательных элементов: " + str(кол_отриц) + "\n")
    f3.write("Среднее арифметическое: " + str(ср_отриц) + "\n\n")

    f3.write("Содержимое второго файла:\n")
    f3.write(" ".join(map(str, числа2)) + "\n")
    f3.write("Положительные элементы: " + " ".join(map(str, положительные)) + "\n")
    f3.write("Количество положительных элементов: " + str(кол_полож) + "\n")
    f3.write("Сумма положительных элементов: " + str(сумма_полож) + "\n")